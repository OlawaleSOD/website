			<h5>From Inaccurate to Precise</h5>
			<h2>Better Understanding Where the Rider Is</h2>
			<p>A major reason problematic pickups occurred were because getting an Uber relied heavily on the Rider explicitly setting a precise pickup location to meet. Not doing so, causes the Driver to be sent to the default device location, which half the time is wildly inaccurate.</p>
			<p>Our data revealed that:</p>
			<ol>
				<li>50% of Riders globally don't explicitly set a pickup location.</li>
				<li>Half of all sessions are at least 100m inaccurate.</li> 
				<li>45% of sessions have an improved GPS accuracy by the time of request. We do nothing with this information.</li>
			</ol>
			<p>Based on these insights, I proposed two key feature ideas <span class="italics">Destination First</span> and <span class="italics">Live Locations</span> to help better understand where the Rider is. Central to the features, were these key ideas:</p>
			<ol>
				<li>Stop relying on the Rider setting their pickup location. Do the heavy lifting on behalf of the Rider.</li>
				<li>Carve out more time for the GPS to warm up.</li>
				<li>Capitalize on GPS updates for as long as possible.</li>
			</ol>
			<h3>Starting at the end</h3>	
			<p>In order to optimize the pickup experience, there was only one thing we needed to know&#8212;the Rider's destination.</p>
			<p>Without much debate, the team agreed that asking “Where to?” upon app launch matched the mental of the Rider. It was also useful in unlocking more advantages, such as providing the user with upfront fares, estimated arrival times and allowing more time for the device GPS to warm up to aid the pickup experience.</p>
			<p>To compensate for the new speed bump, I designed the <span class="italics">accelerators</span> feature&#8212;a predictive set of shortcuts that gives the Rider 1-tap access to their most likely destinations at any given time.</p>
