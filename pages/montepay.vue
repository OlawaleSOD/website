			<h3>Live Locations</h3>
			<p>Live locations feature was born by questioning whether we needed to default to any pickup location at all.</p>
			<p>Rather than lock on the Rider's location upon app launch (which roughly 12-15 seconds for an accurate fix), the app continues sensing, asynchronously updating as the GPS updates. The most accurate GPS location is obtained at the last possible moment&#8212;at the time of request.</p>
			<p>This required a big design shift for the posture of the experience. The challenge was shifting from <span class="italics">set your pickup</span> to <span class="italics">where and how do you want to go?</span></p>
		</div>
		<div class="m-all t-all d2-d9 ld3-ld8 xld3-xld8">
			<figure>
				<img src="http://simonpan.com/wp-content/themes/sp_portfolio/assets/uber-posture.jpg" alt="." />
				<figcaption>Shifting the posture of the UI from setting pickup location, to focussing on where and how the Riders wants to travel.</figcaption>
			</figure>
		</div>
		<div class="m2 t2-t7 d4-d7 ld4-ld7 xld4-xld7">
			<h3>Inspiring confidence on the confirmation screen</h3>
			<p>If the new experience was to do the heavy lifting on behalf of the Rider, the confirmation screen needed to inspire confidence and show how the pickup experience was being taken care of.</p>
			<p>A key design challenge was to consider how much emphasis should be given to this always sensing state.</p>
			<p>The riskier design decisions I made was to obfuscate the pickup location entirely during the core app flow. The idea was to present the overview of the trip to persuade Riders to focus on selecting how they wanted to get there, rather on where to get picked up— since Uber would take care of that.</p>
			<p>This meant the UI needed to inspire confidence for the majority, and those who wanted to assert control needed to know how to discover the feature. It also meant that when we couldn’t optimize on the users behalf, we needed to occasionally ask the user for help.</p>
		</div>
		<div class="m-all t-all d2-d9 ld3-ld8 xld3-xld8">
			<figure>
				<img src="http://simonpan.com/wp-content/themes/sp_portfolio/assets/uber-confirmation.jpg" alt="." />
				<figcaption>Design iterations exploring ways to simplify and alleviate split attention by chunking information and representing location information spatially.</figcaption>
			</figure>
		</div>
		<div class="m2 t2-t7 d4-d7 ld4-ld7 xld4-xld7">
			<h3>Over-designing for anxiety</h3>
			<p>The goal of these designs were to reveal the accuracy of the GPS location as it resolved. I assumed that transparency was the key to building Rider trust. After testing many iterations of this experience, we repeatedly observed that:</p>
			<ol>
				<li>Ambiguity and resolution concepts were mostly missed.</li>
				<li>Riders assumed that the app knew their current location and didn’t actively look to confirm.</li>
			</ol>
			<p>Riders were not anxious or thinking about the GPS accuracy at all. My anxiety, further compounded by stakeholder feedback was being reflected in my designs. The GPS dot and location tooltips were loaded with this anxiety and I felt like I had exhausted all possibilities.</p>
			<p>After the third round of poor testing, I killed the idea in favor of a more simplified and balanced interface that reflected what Riders were focussed on.</p>
			<figure>
				<img src="http://simonpan.com/wp-content/themes/sp_portfolio/assets/uber-confirmation-final.gif" alt="." />
			</figure>
		</div>
	</section>
	<section id="optimized" class="phase mid case-block m-all t-all d-all ld-all xld-all">
		<div class="m2 t2-t7 d4-d7 ld4-ld7 xld4-xld7">
			<h5>From Inefficient to Optimized / Immutable to Fluid</h5>
			<h2>Giving Riders and Drivers back their time</h2>
			<p>Knowing the Rider’s destination and the most accurate understanding of their location allowed us to <span class="italics">do the heavy lifting</span> and select the most optimal pickup location. The system needed to be smart enough to <span class="italics">just work</span> when it could, and humble enough to intervene when it couldn't. This system called <span class="italics">adaptation</span> was by far the most complex problem of this project.</p>
			<p>Foundational to the adaptation framework were these concepts:</p>
			<ol>
				<li>A confidence score would determine whether to choose a spot on behalf of the user, or to disambiguate their location. How we calculated this confidence score would be an ongoing project to improve.</li>
				<li>We needed to respect Rider control and agency. Design patterns needed to allow for opting in, opting out or taking complete control.</li>
				<li>The system needed to be flexible enough to learn about Rider willingness. Don't design based on the assumption we will know the right answer.</li>
			</ol>
		</div>
		<div class="m-all t2-t7 d3-d8 ld3-ld8 xld3-xld8">
			<figure>
				<img src="http://simonpan.com/wp-content/themes/sp_portfolio/assets/uber-adaptationwhiteboard.jpg" alt="" />
				<figcaption>
					Early sketches of the adaptation framework.
				</figcaption>
			</figure>
		</div>
		<div class="m2 t2-t7 d4-d7 ld4-ld7 xld4-xld7">
			<h3>Intelligent pickups</h3>
			<p>Making trips efficient required that we move beyond dispatching the closest car, to dispatching the most optimal* car. This presented several design challenges:</p>
			<ol>
				<li>Getting the Rider to walk to a nearby pickup location.</li>
				<li>Creating heuristics to decide the optimal spot.</li>
				<li>Adapting when we don’t know where the Rider is</li>
				<li>Balancing smart defaults with choice and control.</li>
			</ol>
			<p class="micro italics">*Optimal refers to the car with the fastest route to destination based on a set of nearby candidate pickup locations</p>
		</div>
		<div class="m-all t2-t7 d3-d8 ld3-ld8 xld3-xld8">
			<figure>
				<img src="http://simonpan.com/wp-content/themes/sp_portfolio/assets/uber-challenge.jpg" alt="" />
				<figcaption>
					This shows the time savings from dispatching a car with ideal heading and getting the rider to walk.
				</figcaption>
			</figure>
		</div>
		<div class="m2 t2-t7 d4-d7 ld4-ld7 xld4-xld7">
			<hr />
			<h3>Adaptation - a flexible architecture</h3>
			<p>I designed the flow based on the idea of a location confidence score. If Uber is confident of the Rider's location, it does all the heavy lifting. Otherwise, the app prompts the Rider to validate their location.</p>
			<p>Instead of designing for the right answer, I designed a flexible system optimized for learning and optionality.</p>
