	<section id="obvious" class="phase case-block m-all t-all d-all ld-all xld-all">
		<div class="m2 t2-t7 d4-d7 ld4-ld7 xld4-xld7">
			<h5>From Ambiguous to Obvious</h5>
			<h2>Communicating location in a more intuitive way</h2>
			<p>A critical part of the rendezvous experience is that Riders and Drivers have a clear understanding of where to meet and how to get there. The existing Uber app didn’t do a good job of supporting wayfinding, relying mostly on street addresses and the map.</p>
			<p>During our research trip in India, we observed many wayfinding challenges. Limited street signage, intense traffic on the roads and sidewalks and poor GPS accuracy meant that the Riders and Drivers used POIs to coordinate their pickup location (in spite of what was formed in the app).</p>
			<p>We found that there were structural patterns inherent in their communication, which inspired the Geotalker feature.</p>
		</div>
		<div class="m-all t-all d2-d9 ld3-ld8 xld3-xld8">
			<figure>
				<img src="http://simonpan.com/wp-content/themes/sp_portfolio/assets/uber-place-description.jpg" alt="." />
			</figure>
			<figure>
				<img src="http://simonpan.com/wp-content/themes/sp_portfolio/assets/uber-indiaresearch2.jpg" alt="." />
				<figcaption>Photo credit Scott Silverman</figcaption>
			</figure>
		</div>
		<div class="m2 t2-t7 d4-d7 ld4-ld7 xld4-xld7">
			<h3>Geotalker</h3>
			<p>Geotalker is a place description framework that mirrors how the human mind organizes spatial knowledge. It works by using a set of heuristics to translate a reverse geocoded street address into a <span class="italics">place description</span> phrase to support wayfinding, spatial awareness and communication.</p>
			<p>I started out by creating a concept model for wayfinding and spatial cognition. The exercise helped me come up with the idea that features of the urban environment could have different levels of salience, depending on the point of view of the Rider or Driver.</p>
			<p>The larger vision was to create a system where place description leveraged the POI with the highest level of salience, based primarily on heading direction and other factors. The system would test different labels and self-optimize based on the quality of the pickup experience.</p>
			<p>However, given the speculative nature of the idea and reliance on a quality location data set, we decided to experiment with a less intelligent version in the existing app.</p>
		</div>
		<div class="m-all t-all d-all ld-all xld-all">
			<figure>
				<img src="http://simonpan.com/wp-content/themes/sp_portfolio/assets/uber-geotalker-model.jpg" alt="" />
			</figure>
		</div>
		<div class="m2 t2-t7 d4-d7 ld4-ld7 xld4-xld7">
			<figure>
				<img src="http://simonpan.com/wp-content/themes/sp_portfolio/assets/uber-geotalker-test.jpg" alt="" />
				<figcaption>Left design is the driver app. Right design is the Geotalker experiment in the existing Rider app.</figcaption>
			</figure>
		</div>
		<div class="m2 t2-t7 d4-d7 ld4-ld7 xld4-xld7">
			<h3>Geotalker reduced ambiguity in the pickup experience</h3>
			<p>Early experiments resulted in significant decreases in the distance between pickup request location and actual pickup location and contact rate. We concluded that Geotalker helped to reduce ambiguity of the pickup location.</p>
			<p>This was enough signal necessary to launch the idea in the redesign.</p>
		</div>
